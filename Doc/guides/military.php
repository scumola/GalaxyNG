		|
	    (see Division)</pre>

			<p>E-Division a fast division, normally ships with drives 50% of their mass.</p>

<pre>			Corps
			  |
			  |
		-------------------------........
		|		|		:
		|		|		:
	     Division	      Division	    .........
		|
		|
